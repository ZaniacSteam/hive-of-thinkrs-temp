function getBlogPosts() {
  return blogPosts.sort((a, b) => new Date(b.publishDate).getTime() - new Date(a.publishDate).getTime());
}

function getBlogPost(slug) {
  return blogPosts.find(post => post.slug === slug);
}

function getBlogPostsByCategory(categorySlug) {
  const category = categories.find(cat => cat.slug === categorySlug);
  if (!category) return [];
  
  return blogPosts.filter(post => post.category === category.name);
}

function getRecentPosts(count = 3) {
  return getBlogPosts().slice(0, count);
}
